    private void appendNumero(String numero) {
    if (novoNumero) {
        jTextField1.setText(numero);
        novoNumero = false;
    } else {
        jTextField1.setText(jTextField1.getText() + numero);
    }
}

private void setOperador(String op) {
    try {
        num1 = Double.parseDouble(jTextField1.getText());
        operador = op;
        novoNumero = true;
    } catch (NumberFormatException e) {
        jTextField1.setText("Erro");
    }
}

private void calcular() {
    try {
        num2 = Double.parseDouble(jTextField1.getText());
        double resultado = 0;

        switch (operador) {
            case "+":
                resultado = num1 + num2;
                break;
            case "-":
                resultado = num1 - num2;
                break;
            case "*":
                resultado = num1 * num2;
                break;
            case "/":
                if (num2 == 0) {
                    jTextField1.setText("Erro: รท0");
                    return;
                }
                resultado = num1 / num2;
                break;
            default:
                jTextField1.setText("Operador?");
                return;
        }

        jTextField1.setText(String.valueOf(resultado));
        novoNumero = true;
    } catch (NumberFormatException e) {
        jTextField1.setText("Erro");
    }
}

private void limpar() {
    jTextField1.setText("");
    num1 = num2 = 0;
    operador = "";
    novoNumero = true;
}



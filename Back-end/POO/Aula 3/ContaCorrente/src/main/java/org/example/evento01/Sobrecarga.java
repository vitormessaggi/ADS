package org.example.evento01;

public class Sobrecarga {
    public double somar(double a, double b){
        return a+b;
    }

    public double somar(double a, double b, double c){
        return a+b+c;
    }

    public double somar(double a, double b, double c, double d){
        return a+b+c+d;
    }
}
